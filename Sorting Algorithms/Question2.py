def insertion_sort(list):
    for i in range(1,len(list)):
        e = list[i]
        j = i  - 1
        while j >= 0 and e < list[j]:
            list[j+1] = list[j]
            j = j - 1
        list[j + 1] = e
        print(list)

# #Function call
